/*3. Escreva um algoritmo que imprima os números de 100 à 1 (ordem decrescente).*/
public class rafael03 {
    public static void main(String[] args) {
        
        System.out.println("Números de 100 a 1:");

        for (int n = 100; n >= 1; n = n - 1 ){
            System.out.print(n+", ");
        }
    }
}
